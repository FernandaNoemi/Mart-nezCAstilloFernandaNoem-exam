
import com.sun.xml.rpc.processor.modeler.j2ee.xml.string;


public class asd {
    int peso;
    String cant;
    String result;

    public asd(int peso, String cant, String result) {
        this.peso = peso;
        this.cant = cant;
        this.result = result;
    }

    public int getPeso() {
        return peso;
    }

    public void setPeso(int peso) {
        this.peso = peso;
    }

    public String getCant() {
        return cant;
    }

    public void setCant(String cant) {
        this.cant = cant;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }
    
    
}
